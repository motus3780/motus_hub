-- 모투스 위클리 전환: 브랜드 표기 통일 (Step 10)
-- ----------------------------------------------------------------
-- 목적: settings.sender_name 등 DB에 남아있는 구 브랜드 표기를
--       '모투스 위클리'로 일괄 업데이트
-- ----------------------------------------------------------------
-- 변경 규칙:
--   '모투스컴퍼니 데일리' → '모투스 위클리'
--   '모투스컴퍼니' (단독) → '모투스'
--   '건설·분양 데일리' → '건설·분양 위클리'
-- ----------------------------------------------------------------

-- 1) 발신자 표시 이름
UPDATE settings
   SET value = '모투스 위클리'
 WHERE key = 'sender_name'
   AND value = '모투스컴퍼니 데일리';

-- 2) 기타 동일 패턴 (혹시 다른 settings 키에 박혀있을 수 있는 안전망)
UPDATE settings
   SET value = REPLACE(value, '모투스컴퍼니 데일리', '모투스 위클리')
 WHERE value LIKE '%모투스컴퍼니 데일리%';

UPDATE settings
   SET value = REPLACE(value, '건설·분양 데일리', '건설·분양 위클리')
 WHERE value LIKE '%건설·분양 데일리%';

-- 3) '모투스컴퍼니'가 단독으로 남아있는 경우 → '모투스'
--    (위 두 패턴을 먼저 치환하여 'XX 데일리'를 보존했기 때문에 안전)
UPDATE settings
   SET value = REPLACE(value, '모투스컴퍼니', '모투스')
 WHERE value LIKE '%모투스컴퍼니%';
